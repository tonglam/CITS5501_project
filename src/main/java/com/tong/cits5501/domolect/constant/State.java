package com.tong.cits5501.domolect.constant;

/**
 * Represents the possible states of a device.
 * The states indicate whether the device is currently active or inactive.
 */
public enum State {
  /** 
   * Indicates that the device is currently turned on.
   */
  ON, 
  
  /** 
   * Indicates that the device is currently turned off.
   */
  OFF;
}
