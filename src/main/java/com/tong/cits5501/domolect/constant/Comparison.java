package com.tong.cits5501.domolect.constant;

/**
 * Represents a comparison operation.
 */
public enum Comparison {
  
  /** Indicates that the first value is less than the second value. */
  LESS_THAN,
  
  /** Indicates that the first value is equal to the second value. */
  EQUAL_TO,
  
  /** Indicates that the first value is greater than the second value. */
  GREATER_THAN;
}
